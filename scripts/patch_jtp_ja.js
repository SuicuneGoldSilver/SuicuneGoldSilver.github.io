let trainerPatched = 0, pokemonPatched = 0;

for (const card of cards) {
  if (card.supertype !== 'Pokémon') {
    const t = trainerPatches[card.id];
    if (t !== undefined) { card.textJa = t; trainerPatched++; }
  } else {
    const p = pokemonJaPatches[card.id];
    if (!p) continue;
    if (p.abilities) {
      for (let i = 0; i < p.abilities.length && i < card.abilities.length; i++) {
        if (p.abilities[i].nameJa !== undefined) card.abilities[i].nameJa = p.abilities[i].nameJa;
        card.abilities[i].textJa = p.abilities[i].textJa;
      }
    }
    if (p.attacks) {
      for (let i = 0; i < p.attacks.length && i < card.attacks.length; i++) {
        card.attacks[i].textJa = p.attacks[i].textJa;
      }
    }
    pokemonPatched++;
  }
}

fs.writeFileSync(FILE, BOM + JSON.stringify(cards, null, 2), 'utf8');
